public class SyosutenLiteral{
    public static void main(String[] args){
        /*
        浮動小数点リテラルの書き方
        */

        //32ビットはf・F
        System.out.println(12.345f);

        //64ビットはd・D及び何もない
        System.out.println(12.345);

        //32ビット
        System.out.println(2.1e2f);

        //64ビット
        System.out.println(2.1e2);
    }
}