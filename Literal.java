public class Literal{
    public static void main(String[] args){
        //2進数
        System.out.println(0b1000001);
        //8進数
        System.out.println(0101);
        //10進数
        System.out.println(65);
        //16進数
        System.out.println(0x41);
        //32ビット
        System.out.println(0);
        //64ビット
        System.out.println(0L);
    }
}